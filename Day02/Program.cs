int CalculateScore(Play opponentPlay, Outcome neededOutcome)
{
    var myPlay = GetNeededPlay(neededOutcome, opponentPlay);

    return outcomeScoreMapping[neededOutcome] + playScoreMapping[myPlay];
}

Play GetNeededPlay(Outcome outcome, Play opponentPlay)
{
    return outcome switch
    {
        Outcome.Win => winningCombinations.FirstOrDefault(x => x.Value.Equals(opponentPlay)).Key,
        Outcome.Draw => opponentPlay,
        Outcome.Lose => winningCombinations[opponentPlay]
    };
}

enum Outcome
{
    Win,
    Draw,
    Lose
}
enum Play
{
    Rock,
    Paper,
    Scissors
}