var opponentPlayMapping = new Dictionary<string, Play>
{
    {"A", Play.Rock},
    {"B", Play.Paper},
    {"C", Play.Scissors},
};

var necessaryOutcomeMapping = new Dictionary<string, Outcome>()
{
    { "X", Outcome.Lose },
    { "Y", Outcome.Draw },
    { "Z", Outcome.Win },
};

var outcomeScoreMapping = new Dictionary<Outcome, int>
{
    { Outcome.Win, 6 },
    { Outcome.Draw, 3 },
    { Outcome.Lose, 0 }
};

var playScoreMapping = new Dictionary<Play, int>()
{
    { Play.Rock, 1 },
    { Play.Paper, 2 },
    { Play.Scissors, 3 },
};

var winningCombinations = new Dictionary<Play, Play>
{
    { Play.Rock, Play.Scissors },
    { Play.Paper, Play.Rock },
    { Play.Scissors, Play.Paper }
};

